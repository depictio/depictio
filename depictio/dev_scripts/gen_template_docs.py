"""Translate nf-core ``template.yaml`` files into Markdown reference partials.

Each template is the source of truth for its variables, data collections,
conditional routes, cross-DC links and recipe schemas. Hand-written reference
tables drift out of sync with the template as soon as a DC or recipe changes
(e.g. the ampliseq 2.16.0 template grew to 23 DCs while its doc page still
listed 9). This script reads every template directly and regenerates a
deterministic Markdown partial per template, so the reference always matches
the code with zero hand-maintenance.

The narrative pipeline pages (intro, screenshots, dashboard-tab descriptions)
stay hand-written and *include* the generated partial via an mkdocs snippet:

    ## Reference
    --8<-- "pipeline-templates/nf-core/_generated/ampliseq-2.16.0.md"

So narrative and reference never conflict, and new templates only need a thin
narrative page plus a re-run of this script.

Usage:
    python -m depictio.dev_scripts.gen_template_docs
    python -m depictio.dev_scripts.gen_template_docs --check   # CI / pre-commit
    python -m depictio.dev_scripts.gen_template_docs --docs-root /path/to/depictio-docs
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any

from depictio.cli.cli.utils.templates import _load_yaml
from depictio.models.models.templates import TemplateConditional, TemplateMetadata
from depictio.recipes import load_recipe, resolve_recipe_path

# Repo root: depictio/dev_scripts/gen_template_docs.py -> <repo>/
_REPO_ROOT = Path(__file__).resolve().parents[2]
_GITHUB_BLOB = "https://github.com/depictio/depictio/blob/main"
_DEFAULT_TEMPLATES_ROOT = _REPO_ROOT / "depictio" / "projects" / "nf-core"
# Where generated partials land within the docs repo (relative to its docs/ dir).
_GENERATED_SUBDIR = Path("pipeline-templates") / "nf-core" / "_generated"

_GEN_BANNER = "<!-- GENERATED by depictio/dev_scripts/gen_template_docs.py — do not edit. -->"
_REGEN_HINT = "<!-- Regenerate: python -m depictio.dev_scripts.gen_template_docs -->"

